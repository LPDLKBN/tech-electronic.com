function getSelect(stateObj,country="") {
	var stateListHtml = '';
	if (country=="US") {
		stateListHtml += '<select name="state" id="state" class="form-control">';
		for(var state in stateObj['state']){
			stateListHtml += '<option>'+state+'</option>';
		}
		stateListHtml += '</select>';
	} else {
		stateListHtml +='<input type="text" class="form-control" name="state" id="state" />';
	}
	$('.form-select').html(stateListHtml);
}